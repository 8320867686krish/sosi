    <h2>4.IHM-VSC Onboard Survey</h2>
    <div class="section-1-1">
        <p>After arrival onboard the vessel {{$projectDetail['ship_name']}} , a safety briefing was carried out with ship staff to explain the purpose and scope of the IHM survey explaining the various check points & location from where the visuals/samples must be taken along with safety precaution measures taken while taking samples and marking places as Potentially hazardous materials.</p>

        <p>Our experts are trained and certified see attachment: IHM expert Certificate.</p>

        <p>Adequate safety measures were taken to prevent the spreading of materials, cross-contamination, or the spreading of fibres. At all times these measures are designed to prevent exposure to hazardous materials of workers, our team members, and/or others onboard. The onboard sampling was carried out as per the visual and sampling plan, the equipment, system, and the area selected for sampling were marked in the ship's plan and photographs are recorded.</p>

        <h3>4.1 Safety Precautions & its aspects:</h3>
        <p>Safety precautions while IHM sampling is divided into 3 aspects:</p>
        <ul>
            <li>Precaution for own safety</li>
            <li>Precaution for others’ safety</li>
            <li>Precaution against contamination to Ship & environment.</li>
        </ul>

        <div style="margin-top:10px">
            <p><b>1.Precaution for Own Safety:</b></p>
            <p>Inform the person in charge of the ship & establish communication with the responsible person. Make a risk assessment before initiating the task. Check for the atmosphere, especially in confined spaces. Be equipped with the proper tool which should include but not be limited to: Drilling machine with spare bits </p>
            <ul>
                <li>Cutter with spare blades</li>
                <li>Chisel</li>
                <li>Cutting Pliers</li>
                <li>Permanent marker</li>
                <li>Hammer</li>
                <li>Water spray</li>
                <li>Scrapper</li>
                <li>Hand brush</li>
                <li>Screwdriver</li>
                <li>Torch</li>
                <li>Poly bags</li>
                <li>Plastic bottles which can be sealed properly,</li>
                <li>Scissors</li>
                <li>Masking tape</li>

            </ul>
            <p>
                The use of Mask/Breathing Aid is required to reduce the scattering of hazardous substances, especially asbestos. Check for proper lighting.. Be equipped with proper PPE. It should include but not be limited to:
            </p>
            <ul>
                <li>Boiler suits, additionally disposable suits</li>
                <li>Safety goggles</li>
                <li>Safety shoes, additionally Safety Boots, if necessary, like (entering the ballast tank)</li>
                <li>Ear protection</li>
                <li>Gloves, additionally rubber glove</li>
                <li>Air mask</li>
                <li>Safety helmet</li>
                <li>Gas detector</li>
                <li>Safety harness</li>

            </ul>
            <p>Do not touch any sample with your bare hands. Change boiler suits, disposable suits, and gloves, and take a proper shower.</p>

            <p><b>2.Precaution for Others’ Safety:</b></p>
            <p>Do not put other people at risk. Cordon off the area if required while sampling, especially for asbestos sampling. Inform people in the vicinity & advise safety precautions Check for the people before closing the space. Clean the area after work; make sure any spillage is removed. Seal the sample area to avoid scattering or spillage.</p>

            <p><b>3.Precaution against Contaminations of Ship & Environment: </b></p>
            <p>Sampling precautions must be taken to avoid spillage and contamination of the surrounding spaces. While sampling indoors, avoid strong ventilation but vent afterward. While sampling lubricants and hydraulic oil take care that the system is not under pressure. Cover the space under the sampling area for easy cleaning afterwards Prepare the material against spreading before sampling: moisten solids before breaking or cutting, and use water, oil, or a polymer. Use drill- bags when drilling dry material Re-install the cover after sampling Cover open edges and holes with tape, mastic, or a polymeric coating agent on board, a ship the options to reduce spillage, debris and airborne dust are limited, due to the requirements of the operation. But sampling naturally doesn't cause the same level of contamination as dismantling or large-scale works, for which it is commonly known that safety precautions should be taken. All hazardous materials in coatings are strongly bound in the matrix or incorporated in such a low concentration that release while sampling is normally insignificant. </p>
        </div>

