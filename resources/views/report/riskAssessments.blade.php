        <p>Assessing the risk of a particular scenario should be done in sequence, i.e., first, the potential consequences are estimated, and only thereafter the likelihood of such consequences occurring is assigned. After assessing the consequences, the likelihood is estimated based on historical evidence or experience that such consequences occurred.</p>
        <p>Ensure that the exercise involves people at all levels who are involved in the tasks being discussed as they will understand the problems and know which solutions will and will not work. But remember, you need to include those responsible for planning activities (managers and supervisors) otherwise changes may not happen.</p>

